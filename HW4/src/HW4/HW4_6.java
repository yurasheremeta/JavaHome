package HW4;

public class HW4_6 {
	public static void main(String[] args) {
		String a ="hello";
		System.out.println(hbrv(a));
		String b = "YURA";
		System.out.println(lower(b));
	}
	static String hbrv(String a){
		
		return a.toUpperCase();
	}
	static String lower(String b){
		return b.toLowerCase();
	}

}
